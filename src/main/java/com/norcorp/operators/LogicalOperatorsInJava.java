package com.norcorp.operators;

public class LogicalOperatorsInJava {
    public static void main(String[] args) {
        System.out.println();
        System.out.println("==================================== ☕ JAVA LOGICAL OPERATORS ☕ ====================================");
        System.out.println("☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️️️");

        /*
            Logical operators in Java are used to perform logical operations on boolean values.
            These operators allow you to combine multiple boolean expressions or values and produce a single boolean result.
            Java provides three main logical operators: && (logical AND), || (logical OR), and ! (logical NOT).
         */
        boolean result1 = (true && false); // Result is false
        boolean result2 = (true || false); // Result is true
        boolean result3 = !true; // Result is false

        System.out.println();
        System.out.println(result1);
        System.out.println(result2);
        System.out.println(result3);
        System.out.println();
        System.out.println("☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️️️");
    }
}
