package com.norcorp.literals;

public class LiteralsInJava {
    public static void main(String[] args) {
        System.out.println();
        System.out.println("==================================== ☕ JAVA LITERALS ☕ ====================================");
        System.out.println("☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️️️");

        /*
            In Java, a literal is a representation of a fixed value in source code.
            It is a constant value that can be assigned directly to a variable
            or used as an operand in an expression.
        */

        // Integer Literals:
        int intValue = 42;
        // Floating-Point Literals:
        double doubleValue = 3.14;
        // Character Literals:
        char charValue = 'A';
        // String Literals:
        String stringValue = "Hello";
        // Boolean Literals:
        boolean booleanValue = true;
        /*
            Null Literal: The null literal represents the absence of a value and is used for object references.
            MyClass myObject = null;
        */


        System.out.println();
        System.out.println(intValue);
        System.out.println(doubleValue);
        System.out.println(charValue);
        System.out.println(stringValue);
        System.out.println(booleanValue);
        System.out.println();
        System.out.println("☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️☕️️️");
    }
}
